//! obs: loop em array "iteração"

// For loop
console.log('- For Loop');
const nNames = ['Linus', 'Ada', 'Bill'];
for (let i = 0; i < nNames.length; i++)
{
    console.log(`Elemento ${i}: ${nNames[i]}`);
}
console.log('For loop concluído');
console.log('\n');

// While loop
let i = 0;
while (i < 5)
{
    console.log(`Loop ${i}`);
    i++;
}
console.log('While loop concluído');
console.log('\n');